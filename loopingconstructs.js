//this is the structure of a while loop

//set the counter
var i = 1;

//set the conditional
while (i < 10)
{
    console.log(i); //print so to the console
    i = i + 1; //increment
}


//this is the structure of a forloop

for (j = 11; j < 20; j++) //counter, conditional, and increment all are in the statement
{
    console.log(j);
}